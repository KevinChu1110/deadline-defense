/**
 * @param {object} p
 */
function job(p) {
  return {
    sellRefund: 1,
    traits: p.traits || [],
    quote: p.quote || "上吧！",
    blurb: p.blurb || p.role,
    anim: p.anim || { attackDuration: 0.3, projectile: "bolt", hitStyle: "hit" },
    armorBreakBonus: p.armorBreakBonus || 0,
    series: p.series || "adventurer",
    family: p.family,
    emoji: p.emoji || "⚔️",
    mainStat: p.mainStat || "str",
    weapon: p.weapon || "",
    ...p,
  };
}

