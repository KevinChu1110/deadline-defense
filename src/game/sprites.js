const cache = new Map();

function bake(pixels, palette, scale = 3) {
  const key = `${pixels.join("|")}|${palette.join(",")}|${scale}`;
  if (cache.has(key)) return cache.get(key);

  const rows = pixels;
  const h = rows.length;
  const w = rows[0].length;
  const c = document.createElement("canvas");
  c.width = w * scale;
  c.height = h * scale;
  const ctx = c.getContext("2d");
  ctx.imageSmoothingEnabled = false;

  for (let y = 0; y < h; y++) {
    for (let x = 0; x < w; x++) {
      const ch = rows[y][x];
      if (ch === "." || ch === " " || ch === "0") continue;
      const idx = "123456789ABCDEF".indexOf(ch);
      const color = palette[idx] || palette[0];
      if (!color || color === "transparent") continue;
      ctx.fillStyle = color;
      ctx.fillRect(x * scale, y * scale, scale, scale);
    }
  }
  cache.set(key, c);
  return c;
}

function shade(hex, amount) {
  const h = hex.replace("#", "");
  const full = h.length === 3 ? h.split("").map((c) => c + c).join("") : h;
  let r = parseInt(full.slice(0, 2), 16);
  let g = parseInt(full.slice(2, 4), 16);
  let b = parseInt(full.slice(4, 6), 16);
  r = Math.max(0, Math.min(255, Math.round(r + 255 * amount)));
  g = Math.max(0, Math.min(255, Math.round(g + 255 * amount)));
  b = Math.max(0, Math.min(255, Math.round(b + 255 * amount)));
  return `#${[r, g, b].map((n) => n.toString(16).padStart(2, "0")).join("")}`;
}

/**
 * Palette indices:
 * 1 outline  2 pants  3 clothes  4 hair  5 skin  6 weapon  7 eye  8 hi
 */
function jobPalette(def) {
  const accent = def.color || "#4a7cff";
  const hair = def.hair || shade(accent, -0.4);
  const skin = def.skin || "#f0c8a0";
  return [
    "#2a1f14",
    "#3d2b1f",
    accent,
    hair,
    skin,
    shade(accent, 0.25),
    "#ffffff",
    shade(accent, 0.4),
  ];
}

